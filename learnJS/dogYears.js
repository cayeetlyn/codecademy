//Declares age in human years
let myAge = 20;

let earlyYears = 2;

earlyYears *= 10.5;

let laterYears = myAge - 2;

laterYears *= 4;

//Finishes converting human years to dog years
myAgeInDogYears = earlyYears + laterYears;

//Declares my name and makes it lower case
let myName = "Kat".toLowerCase();

//Logs the results as a full sentence string
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);